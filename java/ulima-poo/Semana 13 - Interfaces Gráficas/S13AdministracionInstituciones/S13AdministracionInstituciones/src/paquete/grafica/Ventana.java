    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int cod = Integer.parseInt(this.jTextField1.getText());
        String nombre = this.jTextField2.getText();
        String ubica = this.jTextField3.getText();
        
        if (this.jRadioButton1.isSelected()) 
        {
            Colegio ref = new Colegio(cod,nombre,ubica);
            ref.CalcularCantAlum();
            GI.Ingresar(ref);
        }
        else if (this.jRadioButton2.isSelected()) 
        {
            Instituto ref = new Instituto(cod,nombre,ubica);
            ref.CalcularCantAlum();
            GI.Ingresar(ref);
        }
        else if (this.jRadioButton3.isSelected()) 
        {
            Universidad ref = new Universidad(cod,nombre,ubica);
            ref.CalcularCantAlum();
            GI.Ingresar(ref);
        }
        
        CargarTabla();
        
    }//GEN-LAST:event_jButton1ActionPerformed

