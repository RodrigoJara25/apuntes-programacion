    private void CargarTabla()
    {
        int fila = this.jTable1.getRowCount();
        //cada vez que se cargue la tabla,se elimina todo y se vuelve a completar
        for (int i = 0; i < fila; i++) 
        {
            modelo.removeRow(0);
        }
        
        Institucion[] arreglo = GI.getArreglo();
        String[] datos = new String[5];
        for (int i = 0; i < GI.getConta(); i++) 
        {            
            datos[0] = String.valueOf(arreglo[i].getCod());
            datos[1] = arreglo[i].getNombre();
            datos[2] = arreglo[i].getUbicacion();
            if (arreglo[i] instanceof Colegio) 
            {
                datos[3] = "Colegio";
            }
            else if (arreglo[i] instanceof Instituto) 
            {
                datos[3] = "Instituto";                
            }
            else if (arreglo[i] instanceof Universidad) 
            {
                datos[3] = "Universidad";                
            }
            datos[4] = String.valueOf(arreglo[i].getCantAlum());
            
            modelo.addRow(datos);
        }
        
    }
    
    
